<template>
  <panel title="search">
    <v-text-field required placeholder="Search by title, artist, albumn or genre" v-model="search">
    </v-text-field>
  </panel>
</template>

<script>
import _ from 'lodash'
export default {
  data () {
    return {
      search: null
    }
  },
  watch: {
    search: _.debounce(function (value) {
      const route = {
        name: 'songs'
      }
      if (this.search !== '') {
        route.query = {
          search: this.search
        }
      }
      this.$router.push(route)
    }, 700),
    '$route.query.search': {
      immediate: true,
      handler (value) {
        this.search = value
      }
    }
  }
}
</script>

